// Feature: image-qa-app
// ApiClient — wraps the fetch call to POST /api/ask
// When deployed on Vercel, set window.API_BASE_URL via a config script or
// use the VITE_/build-time injection. For plain static hosting we read a
// global set by public/config.js (injected in index.html), falling back to
// same-origin (local dev / single-process Railway deploy).
const API_BASE_URL =
  (typeof window !== 'undefined' && window.API_BASE_URL) || '';

/**
 * Send an image and question to the backend /api/ask endpoint.
 *
 * @param {File} image - The validated image file to upload.
 * @param {string} question - The natural-language question.
 * @param {string} requestId - UUID v4 generated by the caller (Req 13.1).
 * @param {boolean} [agenticMode=false] - Whether to enable agentic vision mode.
 * @param {Array<{question: string, answer: string}>} [conversationHistory=[]] - Prior conversation turns.
 * @returns {Promise<{answer: string, requestId: string, steps?: Array}>}
 * @throws {Error} On network failure or non-2xx HTTP response.
 */
export async function ask(image, question, requestId, agenticMode = false, conversationHistory = []) {
  const formData = new FormData();
  formData.append('image', image);
  formData.append('question', question);

  if (agenticMode) {
    formData.append('agenticMode', 'true');
  }

  if (conversationHistory.length > 0) {
    formData.append('conversationHistory', JSON.stringify(conversationHistory));
  }

  let response;
  try {
    response = await fetch(`${API_BASE_URL}/api/ask`, {
      method: 'POST',
      headers: {
        'X-Request-ID': requestId,
      },
      body: formData,
    });
  } catch (networkError) {
    // Network-level failure (no connection, DNS failure, etc.)
    throw new Error('Could not reach the server. Check your connection.');
  }

  let data;
  try {
    data = await response.json();
  } catch {
    throw new Error(`Server returned an unexpected response (HTTP ${response.status}).`);
  }

  if (!response.ok) {
    // Extract human-readable message from JSON body if available
    const message = data?.error
      || `Request failed with status ${response.status}.`;
    throw new Error(message);
  }

  return data;
}
